	public static String replaceSubstring(String original, String oldSub, String newSub) {
        int index = original.indexOf(oldSub);
        
        if (index == -1) {
            // Substring not found, return the original string
            return original;
        }
        
        String modified = original.substring(0, index) + newSub + original.substring(index + oldSub.length());
        return modified;
    }
	
	public static char getNextKanji(char kanji, int increment) {
        int unicode = (int) kanji + increment;
        return (char) unicode;
    }

    public static char getPreviousKanji(char kanji, int decrement) {
        int unicode = (int) kanji - decrement;
        return (char) unicode;
    }
	
